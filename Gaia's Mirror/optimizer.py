class OptimizationGoal:
    """
    Defines a single objective for the optimizer, including its target and weight.
    """
    def __init__(self,
                 name: str,
                 target_metric: str,  # e.g., 'global_temperature_change_c', 'gdp_per_capita', 'human_wellbeing_index'
                 target_value: Optional[float] = None, # e.g., 1.5 (for temp change), 60000 (for GDP)
                 objective_type: str = 'minimize', # 'minimize', 'maximize', 'target'
                 weight: float = 1.0 # Importance weight relative to other goals
                 ):
        self.name = name
        self.target_metric = target_metric
        self.target_value = target_value
        self.objective_type = objective_type
        self.weight = weight

    def evaluate_metric(self, actual_value: float) -> float:
        """
        Calculates a score (or penalty) for a given metric based on the objective type.
        Returns a value where lower is better (for minimization purposes within the optimizer).
        """
        if self.objective_type == 'minimize':
            return actual_value * self.weight
        elif self.objective_type == 'maximize':
            return -actual_value * self.weight # Negate for minimization
        elif self.objective_type == 'target' and self.target_value is not None:
            # Distance from target, squared to penalize large deviations more
            return abs(actual_value - self.target_value) * self.weight
        return 0.0 # Should not happen

class Recommendation:
    """
    Represents a proposed solution or pathway generated by the optimizer.
    """
    def __init__(self,
                 recommendation_id: str,
                 proposed_interventions: List[Dict],
                 simulated_result: SimulationResult,
                 score: Dict[str, float], # Scores against each optimization goal
                 overall_score: float, # Combined score
                 pros_cons: Dict[str, List[str]],
                 risks: List[Dict],
                 ethical_assessment: Dict[str, Any] # Result from CoreEthics post-computation validation
                 ):
        self.recommendation_id = recommendation_id
        self.proposed_interventions = proposed_interventions
        self.simulated_result = simulated_result
        self.score = score
        self.overall_score = overall_score
        self.pros_cons = pros_cons
        self.risks = risks
        self.ethical_assessment = ethical_assessment

    def to_dict(self) -> Dict[str, Any]:
        """Converts the recommendation to a dictionary for storage/logging."""
        return {
            "recommendation_id": self.recommendation_id,
            "proposed_interventions": self.proposed_interventions,
            "simulated_result_id": self.simulated_result.simulation_id, # Only store ID to avoid circular reference
            "score": self.score,
            "overall_score": self.overall_score,
            "pros_cons": self.pros_cons,
            "risks": self.risks,
            "ethical_assessment": self.ethical_assessment
        }

class Optimizer:
    """
    Optimization & Recommendation Engine.
    Analyzes simulation outcomes against configurable goals, generating a spectrum
    of optimized, Pareto-efficient solutions with detailed ethical and risk breakdowns.
    """
    _recommendation_counter: int = 0

    def __init__(self, simulator: Simulator, ethics_core: CoreEthics):
        self.simulator = simulator
        self.ethics_core = ethics_core
        logging.info("Optimizer module initialized, integrated with Simulator and CoreEthics.")

    def _generate_recommendation_id(self) -> str:
        """Generates a unique ID for each recommendation."""
        self.__class__._recommendation_counter += 1
        return f"rec_{datetime.utcnow().strftime('%Y%m%d%H%M%S')}_{self.__class__._recommendation_counter}"

    def _generate_intervention_set(self, base_query: str, num_variations: int) -> List[List[Dict]]:
        """
        MOCK/PLACEHOLDER: Generates a diverse set of possible interventions/policies.
        This would be a sophisticated AI component, potentially leveraging generative models
        or exploring a vast policy space.
        """
        logging.info(f"Generating {num_variations} intervention sets for base query: '{base_query}'")
        intervention_sets = []
        # Example: vary a simple policy parameter
        for i in range(num_variations):
            # Create a base intervention that might achieve the goals, then add variations
            base_interventions = [
                {"type": "carbon_tax", "value": 10 + i * 5, "description": f"Carbon tax of ${10 + i * 5}/ton"},
                {"type": "renewable_investment", "value": 0.01 + i * 0.005, "description": f"Investment of {1 + i * 0.5}% GDP in renewables"}
            ]
            intervention_sets.append(base_interventions)
        
        # Add some "problematic" intervention sets for testing ethical filtering
        if "reduce global temperature" in base_query.lower():
            intervention_sets.append([{"type": "harmful_policy", "description": "Drastic population reduction policy"}])
            intervention_sets.append([{"type": "inequitable_policy", "description": "Energy policy favoring wealthy nations"}])
            intervention_sets.append([{"type": "degradation_policy", "description": "Geoengineering with high environmental risk"}])

        return intervention_sets

    def _evaluate_interventions(self, proposed_interventions: List[Dict], optimization_goals: List[OptimizationGoal]) -> Tuple[SimulationResult, Dict[str, float]]:
        """
        Simulates a set of interventions and evaluates its performance against goals.
        Returns the SimulationResult and a dictionary of scores per goal.
        """
        # Formulate a simulation query from the interventions
        intervention_query = ", ".join([i.get("description", "unspecified intervention") for i in proposed_interventions])
        sim_query = f"Simulate the impact of: {intervention_query}"
        
        # Run the simulation
        # The simulator's internal ethical checks will apply here (pre & post computation)
        sim_result = self.simulator.simulate(sim_query, user_parameters={
            "initial_state": {}, # Would be dynamically fetched from ingest/engine
            "interventions": proposed_interventions,
            "duration_years": 30 # Example duration
        })

        if sim_result.ethical_review_status != "PASSED":
            logging.warning(f"Simulation for interventions ({intervention_query}) was ethically rejected. Status: {sim_result.ethical_review_status}")
            # Assign a very high penalty score to rejected simulations
            scores = {goal.name: float('inf') for goal in optimization_goals}
            overall_score = float('inf')
            return sim_result, {"scores": scores, "overall_score": overall_score}

        # Evaluate against goals
        scores = {}
        overall_score = 0.0
        for goal in optimization_goals:
            metric_value = sim_result.predicted_outcomes.get(goal.target_metric)
            if metric_value is not None:
                score_for_goal = goal.evaluate_metric(metric_value)
                scores[goal.name] = score_for_goal
                overall_score += score_for_goal # Sum of weighted penalties
            else:
                logging.warning(f"Metric '{goal.target_metric}' not found in simulation results for '{intervention_query}'.")
                scores[goal.name] = float('inf') # Penalize missing metrics
                overall_score += float('inf')
        
        return sim_result, {"scores": scores, "overall_score": overall_score}

    def _analyze_pros_cons_risks(self, sim_result: SimulationResult) -> Tuple[Dict[str, List[str]], List[Dict]]:
        """
        MOCK/PLACEHOLDER: Analyzes a simulation result to extract pros, cons, and risks.
        This would leverage causal tracing and domain knowledge.
        """
        pros = []
        cons = []
        risks = []

        # Example logic based on mock outcomes
        if sim_result.predicted_outcomes.get("global_temperature_change_c", 0) < 1.0:
            pros.append("Reduced global temperature rise.")
        if sim_result.predicted_outcomes.get("gdp_per_capita", 0) > 60000:
            pros.append("Increased economic prosperity.")
        if sim_result.predicted_outcomes.get("sea_level_rise_cm", 0) > 20.0:
            cons.append("Significant sea level rise, increasing coastal flood risk.")
            risks.append({"type": "coastal_flooding", "magnitude": sim_result.predicted_outcomes["sea_level_rise_cm"]})
        
        # Ethical concerns found during simulation would also be part of risks/cons
        if sim_result.ethical_review_status == "FLAGGED":
            cons.append(f"Ethical concerns flagged during simulation: {sim_result.ethical_review_details.get('message', '')}")
            risks.append({"type": "ethical_concern", "details": sim_result.ethical_review_details})

        return {"pros": pros, "cons": cons}, risks

    def _identify_pareto_optimal_solutions(self, evaluated_solutions: List[Recommendation]) -> List[Recommendation]:
        """
        Identifies Pareto optimal solutions from a list of recommendations.
        A solution is Pareto optimal if no objective can be improved without
        sacrificing at least one other objective.
        (Simplified implementation for demonstration)
        """
        logging.info("Identifying Pareto optimal solutions (simplified).")
        if not evaluated_solutions:
            return []

        # For simplicity, we'll return the top N solutions based on overall_score,
        # filtering out ethically rejected ones. A true Pareto front requires
        # multi-objective comparison.
        
        # Filter out rejected solutions first
        ethically_sound_solutions = [
            rec for rec in evaluated_solutions
            if rec.ethical_assessment.get("status") == "PASSED"
        ]

        # Sort by overall score (lower is better)
        ethically_sound_solutions.sort(key=lambda x: x.overall_score)
        
        # In a real scenario, this would involve comparing solutions across all objectives
        # to find those on the Pareto front. For now, we take the top few.
        pareto_candidates = []
        if ethically_sound_solutions:
            # Take the best solution based on overall score, and perhaps a few more close ones
            pareto_candidates.append(ethically_sound_solutions[0])
            if len(ethically_sound_solutions) > 1:
                # Add a diverse set if possible, e.g., one that's good on env, another on economy
                # This requires actual multi-objective comparison, not just overall_score
                # For this placeholder, let's just grab the top 3
                pareto_candidates = ethically_sound_solutions[:min(3, len(ethically_sound_solutions))]

        return pareto_candidates


    def optimize(self,
                 base_problem_query: str,
                 optimization_goals: List[OptimizationGoal],
                 num_candidate_interventions: int = 10,
                 duration_years: int = 30
                 ) -> List[Recommendation]:
        """
        Finds optimal and ethically sound recommendations for a given problem.
        """
        logging.info(f"Starting optimization for query: '{base_problem_query}'")
        
        # 1. Generate candidate intervention sets
        candidate_intervention_sets = self._generate_intervention_set(base_problem_query, num_candidate_interventions)
        
        evaluated_recommendations: List[Recommendation] = []

        for interventions in candidate_intervention_sets:
            rec_id = self._generate_recommendation_id()
            logging.info(f"Evaluating candidate intervention set {rec_id}...")

            # 2. Simulate each candidate and get scores
            sim_result, scores_dict = self._evaluate_interventions(interventions, optimization_goals)
            goal_scores = scores_dict["scores"]
            overall_score = scores_dict["overall_score"]

            ethical_assessment = {
                "status": sim_result.ethical_review_status,
                "details": sim_result.ethical_review_details
            }
            
            # If the simulation itself was ethically rejected, record it and move on
            if sim_result.ethical_review_status != "PASSED":
                logging.warning(f"Candidate {rec_id} was ethically rejected during simulation. Skipping full analysis.")
                rejected_recommendation = Recommendation(
                    recommendation_id=rec_id,
                    proposed_interventions=interventions,
                    simulated_result=sim_result,
                    score=goal_scores,
                    overall_score=float('inf'), # Assign infinite penalty
                    pros_cons={"pros": [], "cons": ["Ethically rejected by simulator."]},
                    risks=[{"type": "ethical_rejection", "details": ethical_assessment}],
                    ethical_assessment=ethical_assessment
                )
                evaluated_recommendations.append(rejected_recommendation)
                continue

            # 3. Analyze pros, cons, risks (if simulation passed ethics)
            pros_cons, risks = self._analyze_pros_cons_risks(sim_result)

            # 4. Final Ethical Post-computation Validation on the *recommendation itself*
            # This is a redundant check (simulator already did one), but ensures robustness.
            # Here, we treat the entire recommendation (interventions + outcomes) as a 'proposed_action'
            # for CoreEthics to re-evaluate for its final approval.
            # The simulator already called core_ethics.post_computation_ethical_validation,
            # so we just record the result here.
            
            # If a separate ethical review was needed *after* full recommendation assembly
            # (e.g. if _analyze_pros_cons_risks introduced new ethical context), it would happen here.
            # For now, we rely on the sim_result's ethical_review_status as the final word.
            
            current_recommendation = Recommendation(
                recommendation_id=rec_id,
                proposed_interventions=interventions,
                simulated_result=sim_result,
                score=goal_scores,
                overall_score=overall_score,
                pros_cons=pros_cons,
                risks=risks,
                ethical_assessment=ethical_assessment # This reflects the final status from simulator
            )
            evaluated_recommendations.append(current_recommendation)
            logging.info(f"Evaluated candidate {rec_id}. Overall score: {overall_score:.2f}, Ethical Status: {ethical_assessment['status']}")

        # 5. Identify Pareto optimal solutions (or top few)
        pareto_optimal_recommendations = self._identify_pareto_optimal_solutions(evaluated_recommendations)

        # Log and return only the ethically sound, Pareto-optimal (or top-ranked) solutions
        final_recommendations = [
            rec for rec in pareto_optimal_recommendations
            if rec.ethical_assessment["status"] == "PASSED"
        ]
        
        logging.info(f"Optimization completed. Found {len(final_recommendations)} ethically sound, (quasi)Pareto-optimal recommendations.")
        return final_recommendations

